public class Theater {
    public String theaterName;
    public String theaterImage;
    public String theaterCinemaName;
    public String theaterCinemaAddress;
    public String theaterUrlKey;

    public Theater(String theaterName, String theaterImage, String theaterCinemaName, String theaterCinemaAddress, String theaterUrlKey) {
        this.theaterName = theaterName;
        this.theaterImage = theaterImage;
        this.theaterCinemaName = theaterCinemaName;
        this.theaterCinemaAddress = theaterCinemaAddress;
        this.theaterUrlKey = theaterUrlKey;
    }
}
